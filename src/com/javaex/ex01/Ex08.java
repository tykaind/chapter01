package com.javaex.ex01;

public class Ex08 {

	public static void main(String[] args) {

		//대입연산자
		int a = 7;

		System.out.println(a);  //7
		System.out.println(++a);//8	
		System.out.println(a);	//8
		
		System.out.println(--a); //7
		System.out.println(a--); //7
		System.out.println(a++); //6
		System.out.println(a); //7
		System.out.println(a++ + 3+4); //7,
		System.out.println(a); //15
	}

}
